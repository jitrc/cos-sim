@SuppressWarnings("serial")
public class CompositeDriverFrameworkException extends RuntimeException {

	public CompositeDriverFrameworkException() {
	}

	public CompositeDriverFrameworkException(String arg0) {
		super(arg0);
	}

	public CompositeDriverFrameworkException(Throwable arg0) {
		super(arg0);
	}
	
	public CompositeDriverFrameworkException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
